package com;

public class Komisja {
    private int dzien;
    private int slot;
    private int id_przew;


    public void setDzien(int dzien) {
        this.dzien = dzien;
    }

    public void setSlot(int slot) {
        this.slot = slot;
    }

    public void setId_przew(int id_przew) {
        this.id_przew = id_przew;
    }

    public int getDzien() {
        return dzien;
    }

    public int getSlot() {
        return slot;
    }

    public int getId_przew() {
        return id_przew;
    }
}
